using Unit05.Game.Casting;
using Unit05.Game.Directing;
using Unit05.Game.Scripting;
using Unit05.Game.Services;
using Unit05.Game;


namespace Unit05
{
    /// <summary>
    /// The program's entry point.
    /// </summary>
    class Program
    {
        /// <summary>
        /// Starts the program using the given arguments.
        /// </summary>
        /// <param name="args">The given arguments.</param>
        static void Main(string[] args)
        {
            // create the cast
            Cast cast = new Cast();
            cast.AddActor("cycle1", new Snake(Constants.YELLOW, new System.Numerics.Vector2(Constants.CELL_SIZE * 4, Constants.CELL_SIZE * 4)));
            cast.AddActor("score", new Score());
            cast.AddActor("cycle2", new Snake(Constants.RED, new System.Numerics.Vector2(Constants.CELL_SIZE * 5, Constants.CELL_SIZE * 5)));

            // create the services
            KeyboardService keyboardService = new KeyboardService();
            VideoService videoService = new VideoService(false);
           
            // create the script
            Script script = new Script();
            script.AddAction("input", new ControlPlayerOne(keyboardService));
            script.AddAction("input", new ControlPlayerTwo(keyboardService));
            script.AddAction("update", new MoveActorsAction());
            script.AddAction("update", new HandleCollisionsAction());
            script.AddAction("output", new DrawActorsAction(videoService));

            // start the game
            Director director = new Director(videoService);
            director.StartGame(cast, script);
        }
    }
}